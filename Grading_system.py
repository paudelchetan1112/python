percentage=float(input("Enter number in percentage:"))
if percentage>=35:
    print("passing Grade")
if percentage>=90:
    print("Grade A+")
elif percentage>=80:
    print("Grade A")
elif percentage>=70:
    print("Grade B+")
elif percentage>=60:
    print("Grade B")
elif percentage>=50:
    print("Grade C+")
elif percentage>=40:
    print("Grade C")
elif percentage>=35:
    print("Grade D")
else:
    print("Non Grade")